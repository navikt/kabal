export const getDuaAccessError = (
  user: DuaAccessUser,
  caseStatus: DuaAccessCaseStatus,
  documentType: DuaAccessDocumentType,
  parent: DuaAccessParent,
  creator: DuaAccessCreator,
  action: DuaActionEnum,
): string | null => {
  const key = `${user}:${caseStatus}:${documentType}:${parent}:${creator}:${action}`;
  const access = ACCESS_MAP[key];

  if (access === undefined) {
    return `"${key}" skal ikke være mulig. Kontakt Team Klage.`;
  }

  if (access === DuaAccessEnum.ALLOWED) {
    return null;
  }

  return ERROR_MESSAGE_MAP[`${access}:${action}`] ?? null;
};

